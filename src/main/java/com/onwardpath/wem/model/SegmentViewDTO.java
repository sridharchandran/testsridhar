package com.onwardpath.wem.model;

public class SegmentViewDTO {

	private String segtype;
	
	private String segbev;
	
	private String segtech;
	
	private String segint;
	
	private String segref;
	
	private int offset;
	
	private int limit;

	private String search;
	
	public String getSegtype() {
		return segtype;
	}

	public void setSegtype(String segtype) {
		this.segtype = segtype;
	}

	public String getSegbev() {
		return segbev;
	}

	public void setSegbev(String segbev) {
		this.segbev = segbev;
	}

	public String getSegtech() {
		return segtech;
	}

	public void setSegtech(String segtech) {
		this.segtech = segtech;
	}

	public String getSegint() {
		return segint;
	}

	public void setSegint(String segint) {
		this.segint = segint;
	}

	public String getSegref() {
		return segref;
	}

	public void setSegref(String segref) {
		this.segref = segref;
	}

	public int getOffset() {
		return offset;
	}

	public void setOffset(int offset) {
		this.offset = offset;
	}

	public int getLimit() {
		return limit;
	}

	public void setLimit(int limit) {
		this.limit = limit;
	}

	public String getSearch() {
		return search;
	}

	public void setSearch(String search) {
		this.search = search;
	}
	
	
	
}
